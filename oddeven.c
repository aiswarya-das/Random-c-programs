
/* To calculate whether a given number is odd or even */

#include<stdio.h>
int main () {
    int num ;
    printf("Enter a number:");
    scanf("%d",&num);
    if (num % 2 ==0)
    {
        printf("The given number is even\n");
    }
    else
    {
        printf("The given number is odd\n");
    }
    
    
}



















/* Output :
Enter a number : 5
Number is odd */



